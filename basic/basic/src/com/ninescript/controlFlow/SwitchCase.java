public class SwitchCase 
{
	public static void main(String[] args)
	{
		
	
	int b=1000;
	switch(b)
	{
	case 10:	
				System.out.println(10);
				break;
	case 20:
				System.out.println(20);
				break;
	case 1000:
				System.out.println(1000);
				break;
	}

}
}

