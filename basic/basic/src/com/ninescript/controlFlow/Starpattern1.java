public class Starpattern1

	{
		public static void main(String asd[])
		{
			
			int i,j;
			for( i=0;i<=5;i++)
			{
				for ( j=5;j>=i;j--)
				{	
					System.out.print("*");
				}
					System.out.println("");
			}
			
			
			
			
		}
	}

