package com.ninescript.test.strings.basicdemo1;

public class StringToLowerCaseExample {
	
	public static void main(String[] args) {

		String str = "PIYUSH AGARKAR";//This String Will Converted into Lower Case

		
		String strLower = str.toLowerCase();

		System.out.println("Original String: " + str);
		System.out.println("String changed to lower case: " + strLower);
		
		
		
		
		
		
		
	}

}
