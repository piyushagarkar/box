package com.ninescript.test.strings.basicdemo1;

public class StringEndsWithExample {
	
	public static void main(String[] args) {

	
		String strOrig = "Piyush Agarkar";

	
		if (strOrig.endsWith("Agarkar")) {
			System.out.println("String ends with Agarkar");
		} else {
			System.out.println("String does not end with Agarkar");
		}

	}

}
